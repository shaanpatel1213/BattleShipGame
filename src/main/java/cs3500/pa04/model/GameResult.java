package cs3500.pa04.model;

/**
 * GameResult is all the results the game can end in.
 * The game can end in one of : WIN, LOSE, DRAW.
 */
public enum GameResult {
  /**
   * player had won the game
   */
  WIN,
  /**
   * player had lost the game
   */
  LOSE,
  /**
   * the game ended in a draw
   */
  DRAW,
}
